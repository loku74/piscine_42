#include <unistd.h>

void	ft_is_negative(int n)
{
	int	positive;
	int	negative;

	positive = 80;
	negative = 78;
	if (n >= 0)
	{
		write(1, &positive, 1);
	}
	else
	{
		write(1, &negative, 1);
	}
}
